public class IfTest3 {
  public static void main(String[] args){
    int myAge = 36;
    if(myAge == 35){
      System.out.println("私の年齢は35歳！");
    } else if(myAge >= 40){
      System.out.println("私は40歳以上です！");
    }else{
      System.out.println("私は35歳でも40歳以上ではないです！");
    }
    System.out.println("私の出身地は・・・");
  }
}
